  var langNames = { en: "EN", zh: "中文", ja: "日本語", de: "DE", ko: "한국어" };

  function getText(key) {
    var lang = t[currentLang];
    if (lang && lang[key] !== undefined) return lang[key];
    if (t.en[key] !== undefined) return t.en[key];
    return key;
  }

  function apply() {
    var year = new Date().getFullYear();
    document.querySelectorAll('[data-i18n]').forEach(function (el) {
      var key = el.getAttribute('data-i18n');
      var text = getText(key);
      if (text !== undefined) {
        el.innerHTML = text.replace(/\{year\}/g, year);
      }
    });
    var langLabel = document.getElementById('currentLang');
    if (langLabel) langLabel.textContent = langNames[currentLang] || currentLang.toUpperCase();
    document.documentElement.lang = currentLang;
  }

  function setLang(lang) {
    if (!t[lang]) return;
    currentLang = lang;
    localStorage.setItem('lang', lang);
    apply();
  }

  apply();

  $(function () {
    $('[data-lang]').on('click', function (e) {
      e.preventDefault();
      setLang($(this).data('lang'));
    });
  });

  return { getLang: function () { return currentLang; }, setLang: setLang, apply: apply };
